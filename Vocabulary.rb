#Vocabulary

Test-Driven Development (TDD)
TDD is a process for writing software. Its basic steps are as follows:

Add a test
Run all the tests and make sure the new test fails
Write code
Run all the tests and make sure the new test passes
Refactor the code.

#Behavior-Driven Development (BDD)

BDD is an extension of TDD. It structures the testing process through the use of user
stories - a description of the user's' use of the feature being developed. The goal of the
user story is to help focus the developer on what to test and what not to test.

#Unit Test

Unit testing is a way of testing an application by breaking the application down into its
smallest constituent parts and testing each part in isolation. In order to test parts of an
application that rely on other code, unit tests typically use mock objects to make sure
that problems elsewhere in the code won't' cause the tests to mistakenly fail.

Unit testing is often automated at larger or more sophisticated companies.

#Integration Test

Integration testing is a form of testing in which units of code are combined
("integrated") and the results of their interactions are tested. This type of testing is done
after each of the individual units has been tested.

#Test Coverage
"Test coverage" refers to how much of a software program has been tested. This can
refer to several things, including the percentage of functions that have been called by
the test suite, or the percentage of statements in the code that have been executed by
the test suite.